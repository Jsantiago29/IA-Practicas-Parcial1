# Eliminamos el segundo elemento de la lista (el elemento en el índice 1), que es 'azul'
del colores[1]

# Eliminamos el cuarto elemento de la lista (el elemento en el índice 3), que es 'amarillo'
del colores[3]

# Eliminamos el quinto elemento de la lista (el elemento en el índice 4), que es 'marrón'
del colores[4]

# Eliminamos el tercer elemento desde el final de la lista, que es 'rosa'
del colores[-3]

# Imprimimos la lista actualizada de 'colores' después de haber eliminado los elementos
