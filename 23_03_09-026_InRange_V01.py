# Se utiliza un bucle for para iterar a través de una secuencia de números generada por la función range
# La secuencia comienza en 7, termina en 700 y los elementos están separados por un intervalo de 100
for x in range(7, 700, 100):
	# En cada iteración, se imprime el valor actual de x
	print(x)
