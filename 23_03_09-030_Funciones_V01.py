# Se define una función llamada "suma" que toma dos parámetros: numero1 y numero2
def suma(numero1, numero2):
	# Dentro de la función, se realiza la suma de los dos parámetros
	print(numero1 + numero2)

# Se llaman a la función "suma" tres veces con diferentes argumentos
# En cada llamada, la función realiza la suma de los dos argumentos y se imprime el resultado en la consola
suma(10, 20)
suma(20, 30)
suma(50000, 7000)
