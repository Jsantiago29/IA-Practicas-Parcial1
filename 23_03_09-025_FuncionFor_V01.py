# Se define una tupla con cuatro elementos de tipo string que representan diferentes colores
colores = ('rojo', 'azul', 'verde', 'amarillo')

# Se utiliza un bucle for para iterar a través de cada elemento en la tupla colores
for x in colores:
	# En cada iteración, se imprime un mensaje que indica el color actual del bucle
	print('El color es: ' + x + '.')
