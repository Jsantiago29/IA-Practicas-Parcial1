# Definimos una variable de tipo string llamada "mensaje1" y le asignamos el valor "Jaime"
mensaje1 = 'Jaime'

# Definimos una variable de tipo string llamada "mensaje2" y le asignamos el valor " Santiago"
mensaje2 = ' Santiago'

# Definimos una variable llamada "nombrecompleto" y le asignamos el valor de la concatenación de "mensaje1" y "mensaje2"
# Como "mensaje1" es "Jaime" y "mensaje2" es " Santiago", la variable "nombrecompleto" será "Jaime Santiago"
