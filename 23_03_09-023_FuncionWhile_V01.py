# Se inicializa la variable x con el valor 0
x = 0

# Mientras x sea menor o igual a 15, se imprimirá el valor de x y se le sumará 5 a x en cada iteración
while x <= 15 :
    print(x)
    x += 5
    
# Se inicializa la variable y con el valor 0
y = 0

# Mientras y sea mayor o igual a -100, se imprimirá el valor de y y se le restará 20 a y en cada iteración
while y >= -100:
    print(y)
    y -= 20
    
# Se inicializa la variable z con el valor 10
z = 10

# Mientras z sea mayor o igual a 0, se imprimirá el mensaje "El valor de z es: " seguido del valor de z, y se le restará 1 a z en cada iteración
while z >= 0:
    print('El valor de z es: ', z)
    z -= 1
