# Definimos una variable de tipo string llamada "minombre" y le asignamos el valor "jaime santiago garcia"
# Luego, aplica el método "title()" a la variable para capitalizar la primera letra de cada palabra en la cadena.
# El resultado se almacena nuevamente en la variable "minombre".
minombre = 'jaime santiago garcia'.title()

# Imprime el valor de la variable "minombre" en la consola
print(minombre)

# Definimos una variable de tipo string llamada "frase1" y le asignamos el valor "Esta Es Una Frase Para Ser Formateada"
# Luego, aplica el método "lower()" a la variable para convertir todas las letras en la cadena a minúsculas.
# El resultado se almacena nuevamente en la variable "frase1".
frase1 = 'Esta Es Una Frase Para Ser Formateada'.lower()

# Imprime el valor de la variable "frase1" en la consola
print(frase1)

# Definimos una variable de tipo string llamada "frase2" y le asignamos el valor "Esta Es Una Frase Para Ser Formateada"
# Luego, aplica el método "upper()" a la variable para convertir todas las letras en la cadena a mayúsculas.
# El resultado se almacena nuevamente en la variable "frase2".
frase2 = 'Esta Es Una Frase Para Ser Formateada'.upper()

# Imprime el valor de la variable "frase2" en la consola
print(frase2)
