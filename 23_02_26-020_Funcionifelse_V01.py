# Se define una variable 'color' y se le asigna el valor 'rojo'
color = 'rojo'

# Se evalúa si el valor de la variable 'color' es diferente de 'rojo' usando una estructura condicional if
if color != 'rojo':
    # Si la condición es verdadera, se ejecuta este código
    print('El color no es rojo')
else:
    # Si la condición es falsa, se ejecuta este código
    print('El color si es rojo')
