# Asignación de valores a dos variables numéricas
num1 = 15
num2 = 20

# Se evalúa si num1 es menor que num2 usando una estructura condicional if
if num1 < num2:
    # Si se cumple la condición, se ejecuta el código dentro del if
    print('Se ejecuta el if.')
    
#-----------------------------------------------------------------------------------------------

# Asignación de valores a otras dos variables numéricas
